const a = 10
const b = 12

console.log('Menor que -> <')

if (a < b) console.log('10 es menor que 12')

console.log('Mayor que -> >')

if (b > a) console.log('12 es mayor que 10')

console.log('Menor o igual que -> <=')

if (a <= b) console.log('10 es menor o igual que 12')

console.log('Mayor o igual que -> >=')

if (b >= a) console.log('12 es mayor o igual que 10')